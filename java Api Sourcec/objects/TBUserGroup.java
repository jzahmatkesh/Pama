package objects;

public class TBUserGroup {
	private int grpid;
	private String grpname;
	private int prvid;
	private String prvname;
	private int userid;
	private String usrfamily;
	private String token;
	
	public int getGrpid() {
		return grpid;
	}
	public void setGrpid(int grpid) {
		this.grpid = grpid;
	}
	public String getGrpname() {
		return grpname;
	}
	public void setGrpname(String grpname) {
		this.grpname = grpname;
	}
	public int getPrvid() {
		return prvid;
	}
	public void setPrvid(int prvid) {
		this.prvid = prvid;
	}
	public String getPrvname() {
		return prvname;
	}
	public void setPrvname(String prvname) {
		this.prvname = prvname;
	}
	public int getUserid() {
		return userid;
	}
	public void setUserid(int userid) {
		this.userid = userid;
	}
	public String getUsrfamily() {
		return usrfamily;
	}
	public void setUsrfamily(String usrfamily) {
		this.usrfamily = usrfamily;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	
	
}
