package objects;

public class TBAddInfo {
	private int id;
	private String name;
	private int kind;
	private int dublicate;
	private String token;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getKind() {
		return kind;
	}
	public void setKind(int kind) {
		this.kind = kind;
	}
	public int getDublicate() {
		return dublicate;
	}
	public void setDublicate(int dublicate) {
		this.dublicate = dublicate;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	
	
}
